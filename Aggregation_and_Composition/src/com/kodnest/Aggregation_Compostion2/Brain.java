package com.kodnest.Aggregation_Compostion2;

public class Brain {
	String colour;
	int weight;
	public Brain(String colour, int weight) {
		super();
		this.colour = colour;
		this.weight = weight;
	}
	public String getColour() {
		return colour;
	}
	
	public int getWeight() {
		return weight;
	}
	

}
