package com.kodnest.Aggregation_Compostion;

public class Charger {
	String colour;
	String Brand;
	String type;
	public Charger(String colour,String Brand, String type)
	{
		this.colour=colour;
		this.Brand=Brand;
		this.type=type;
	}
	public String getColour() {
		return colour;
	}
	public String getBrand() {
		return Brand;
	}
	public String getType() {
		return type;
	}



}
